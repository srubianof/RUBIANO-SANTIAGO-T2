package edu.eci.arsw.weather.model;

public class Wind {
    private double speed;
    private int deg;

    public Wind() {
    }

    public double getSpeed() {
        return speed;
    }

    public void setSpeed(double speed) {
        this.speed = speed;
    }

    public int getDeg() {
        return deg;
    }

    public void setDeg(int ged) {
        this.deg = ged;
    }
}
